create view v_equipe_coureur as 
SELECT
	EC.COUREUR,
	EC.EQUIPE,
	CR.NOM AS NOM_COUREUR,
	CR.GENRE,
	CR.DTN,
	E.PSEUDO AS PSEUDO_EQUIPE,
	E.NOM AS NOM_EQUIPE
FROM
	EQUIPE_COUREUR AS EC
	JOIN COUREUR AS CR ON EC.COUREUR = CR.NUMERO_DOSARD
	JOIN EQUIPE AS E ON EC.EQUIPE = E.ID_EQUIPE;
