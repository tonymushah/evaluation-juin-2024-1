create VIEW v_temps_coureur_etape_equipe_coureur as 
SELECT
	TCE.ETAPE,
	TCE.LONGUEUR,
	TCE.NOM,
	TCE.NB_COUREUR_PAR_EQUIPE,
	TCE.TEMPS,
	TCE.POINTS,
	TCE.EQUIPE_COUREUR,
	TCE.DEPART,
	EC.COUREUR,
	EC.EQUIPE
FROM
	V_TEMPS_COUREUR_ETAPE AS TCE
	JOIN EQUIPE_COUREUR AS EC ON TCE.EQUIPE_COUREUR = EC.ID_EQUIPE_COUREUR;