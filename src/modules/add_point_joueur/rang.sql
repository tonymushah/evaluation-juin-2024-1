SELECT
	CURRENT_RANG.RANG,
	CURRENT_RANG.ETAPE,
	CURRENT_RANG.EQUIPE,
	CURRENT_RANG.EQUIPE_COUREUR,
	CURRENT_RANG.COUREUR,
	PTS.VALEUR AS POINTS
FROM
	(
		SELECT
			DENSE_RANK() OVER (
				ORDER BY
					TEMPS ASC
			) RANG,
			ETAPE,
			COUREUR,
			EQUIPE,
			EQUIPE_COUREUR
		FROM
			V_TEMPS_COUREUR_ETAPE_EQUIPE_COUREUR
		WHERE
			ETAPE = {}
			AND TEMPS IS NOT NULL
		GROUP BY
			ETAPE,
			TEMPS,
			COUREUR,
			EQUIPE,
			EQUIPE_COUREUR
	) AS CURRENT_RANG
	LEFT JOIN POINTS AS PTS ON CURRENT_RANG.RANG = PTS.RANG